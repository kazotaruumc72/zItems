package fr.traqueur.items.api.effects.drops;

/**
 * Enum representing possible drop locations for items.
 */
public enum DropLocation {

    /**
     * Drop at the location of a block.
     */
    BLOCK,
    /**
     * Drop at the center of an area.
     */
    CENTER,
    /**
     * Drop at the player's location.
     */
    PLAYER
}