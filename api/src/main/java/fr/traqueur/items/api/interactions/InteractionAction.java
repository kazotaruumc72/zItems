package fr.traqueur.items.api.interactions;

/**
 * Enum representing different types of interaction actions.
 */
public enum InteractionAction {
    /**
     * Represents a shift + left click action.
     */
    SHIFT_LEFT_CLICK,
    /**
     * Represents a shift + right click action.
     */
    SHIFT_RIGHT_CLICK,
    /**
     * Represents a right click action.
     */
    RIGHT_CLICK,
    /**
     * Represents a left click action.
     */
    LEFT_CLICK,
    /**
     * Represents a simple click action.
     */
    CLICK
}
